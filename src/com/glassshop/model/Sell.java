package com.glassshop.model;

public class Sell {
	private String sellId;
	private String orderGoodsId;
	private String sellDate;
	private String sellQuantity;
	private String sellCost;
	private String sellCostSum;
	private String sellNote;
	private String sellRecordDate;
	public String getSellId() {
		return sellId;
	}
	public void setSellId(String sellId) {
		this.sellId = sellId;
	}
	public String getOrderGoodsId() {
		return orderGoodsId;
	}
	public void setOrderGoodsId(String orderGoodsId) {
		this.orderGoodsId = orderGoodsId;
	}
	public String getSellDate() {
		return sellDate;
	}
	public void setSellDate(String sellDate) {
		this.sellDate = sellDate;
	}
	public String getSellQuantity() {
		return sellQuantity;
	}
	public void setSellQuantity(String sellQuantity) {
		this.sellQuantity = sellQuantity;
	}
	public String getSellCost() {
		return sellCost;
	}
	public void setSellCost(String sellCost) {
		this.sellCost = sellCost;
	}
	public String getSellCostSum() {
		return sellCostSum;
	}
	public void setSellCostSum(String sellCostSum) {
		this.sellCostSum = sellCostSum;
	}
	public String getSellNote() {
		return sellNote;
	}
	public void setSellNote(String sellNote) {
		this.sellNote = sellNote;
	}
	public String getSellRecordDate() {
		return sellRecordDate;
	}
	public void setSellRecordDate(String sellRecordDate) {
		this.sellRecordDate = sellRecordDate;
	}
	
	
}
