package com.glassshop.model;

public class SalesMan {
	
	private String salesmanId;
	private String vendorId;
	private String salesmanName;
	private String salesmanTel;
	private String vendorName;
	private String vendorTel;
	
	
	public String getVendorName() {
		return vendorName;
	}
	public void setVendorName(String vendorName) {
		this.vendorName = vendorName;
	}
	public String getVendorTel() {
		return vendorTel;
	}
	public void setVendorTel(String vendorTel) {
		this.vendorTel = vendorTel;
	}
	
	
	public String getSalesmanId() {
		return salesmanId;
	}
	public void setSalesmanId(String salesmanId) {
		this.salesmanId = salesmanId;
	}

	public String getVendorId() {
		return vendorId;
	}
	public void setVendorId(String vendorId) {
		this.vendorId = vendorId;
	}
	public String getSalesmanName() {
		return salesmanName;
	}
	public void setSalesmanName(String salesmanName) {
		this.salesmanName = salesmanName;
	}
	public String getSalesmanTel() {
		return salesmanTel;
	}
	public void setSalesmanTel(String salesmanTel) {
		this.salesmanTel = salesmanTel;
	}
	
	
}
